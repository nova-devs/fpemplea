<?php

namespace AppBundle\Entity;

/**
 * Tiposoferta
 */
class Tiposoferta
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $nombre;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return Tiposoferta
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }
    
    //Metodo MAGICO creado para devolver el nombre del tipo de oferta
    public function __toString() {
        return $this->nombre;
    }
}
